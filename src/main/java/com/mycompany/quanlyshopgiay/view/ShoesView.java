     public void showShoesList(List<Shoes> listGiay) {
        NumberFormat formatter = NumberFormat.getInstance(new Locale("vi", "VN"));

        // ➤ Thêm cột "Hình ảnh"
        String[] columnNames = {
            "Mã giày", "Tên giày", "Hãng", "Size", "Màu sắc", 
            "Giá", "Số lượng tồn", "Ngày nhập", "Hình ảnh"
        };

        DefaultTableModel model = new DefaultTableModel(columnNames, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        double tongTienKho = 0;
        int tongSoLuongTon = 0;

        for (Shoes s : listGiay) {
            model.addRow(new Object[] {
                s.getMaGiay(),
                s.getTenGiay(),
                s.getHang(),
                s.getSize(),
                s.getMauSac(),
                formatter.format(s.getGia()),
                s.getSoLuongTon(),
                new SimpleDateFormat("yyyy-MM-dd").format(s.getNgayNhap()),
                s.getHinhAnh()  // ➤ THÊM dòng ảnh
            });
            tongTienKho += s.getGia() * s.getSoLuongTon();   // 💰 Tính tổng tiền
            tongSoLuongTon += s.getSoLuongTon();  
        }
                // 📦 Tính tổng tồn kho

        tblShoes.setModel(model);

        // ➤ Ẩn cột ảnh (chỉ số 8)
        tblShoes.getColumnModel().getColumn(8).setMinWidth(0);
        tblShoes.getColumnModel().getColumn(8).setMaxWidth(0);
        tblShoes.getColumnModel().getColumn(8).setWidth(0);

        this.listShoes = listGiay;
        
        tonKho.setText("Tổng tồn kho: " + tongSoLuongTon);
        tongTienKhoGiay.setText("Tổng tiền: " + formatter.format(tongTienKho));
      
    }

   




