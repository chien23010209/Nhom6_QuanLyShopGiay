    public Shoes getSelectedShoeFromTable() throws ParseException {
        int row = tblShoes.getSelectedRow();
        if (row == -1) return null;

        String ma = tblShoes.getValueAt(row, 0).toString();
        String ten = tblShoes.getValueAt(row, 1).toString();
        String hang = tblShoes.getValueAt(row, 2).toString();
        int size = Integer.parseInt(tblShoes.getValueAt(row, 3).toString());
        String mau = tblShoes.getValueAt(row, 4).toString(); // ✅ Lấy từ bảng
        double gia = NumberFormat.getInstance().parse(tblShoes.getValueAt(row, 5).toString()).doubleValue();
        int soLuong = Integer.parseInt(tblShoes.getValueAt(row, 6).toString());
        String ngayNhapStr = tblShoes.getValueAt(row, 7).toString();
        Date date = new SimpleDateFormat("yyyy-MM-dd").parse(ngayNhapStr);

        String hinh = tblShoes.getValueAt(row, 8).toString(); // lấy từ cột ảnh


        return new Shoes(ma, ten, hang, size, mau, gia, date, soLuong, hinh);
    }



    public void fillForm(Shoes s) {
        txtMaGiay.setText(s.getMaGiay());
        txtTenGiay.setText(s.getTenGiay());
        txtHang.setText(s.getHang());
        spinnerSize.setValue(s.getSize());
        spinnerSoLuong.setValue(s.getSoLuongTon());
        NumberFormat formatter = NumberFormat.getInstance(new Locale("vi", "VN"));
        txtGia.setText(formatter.format(s.getGia()));
        txtMauSac.setText(s.getMauSac());
        dateChooserNgayNhap.setDate(s.getNgayNhap());
        txtHinhAnh.setText(s.getHinhAnh());

        File f = new File(s.getHinhAnh());

        // Nếu file không tồn tại → thử nối với thư mục dự án
        if (!f.exists()) {
            String path = new File("").getAbsolutePath() + File.separator + s.getHinhAnh();
            f = new File(path);
        }

        // Kiểm tra lại tồn tại sau khi chỉnh path
        if (f.exists()) {
            ImageIcon icon = new ImageIcon(f.getAbsolutePath());
            Image img = icon.getImage().getScaledInstance(lblAnh.getWidth(), lblAnh.getHeight(), Image.SCALE_SMOOTH);
            lblAnh.setIcon(new ImageIcon(img));
        } else {
            lblAnh.setIcon(null);
            System.out.println("❌ Không tìm thấy ảnh: " + f.getAbsolutePath());
        }



        tblShoes.scrollRectToVisible(tblShoes.getCellRect(tblShoes.getSelectedRow(), 0, true));
    }


