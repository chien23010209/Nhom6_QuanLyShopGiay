    private void btnChonAnhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonAnhActionPerformed
        JFileChooser chooser = new JFileChooser();
        int result = chooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();

            // 🚀 Lưu đường dẫn tương đối (so với thư mục project)
            String projectPath = new File("").getAbsolutePath();
            String relativePath = file.getAbsolutePath().replace(projectPath + File.separator, "");
            txtHinhAnh.setText(relativePath); // -> ví dụ: images/abc.jpg

            // 🖼️ Hiển thị ảnh lên lblAnh
            ImageIcon icon = new ImageIcon(file.getAbsolutePath());
            Image img = icon.getImage().getScaledInstance(
                lblAnh.getWidth(), lblAnh.getHeight(), Image.SCALE_SMOOTH
            );
            lblAnh.setIcon(new ImageIcon(img));
        }
    }//GEN-LAST:event_btnChonAnhActionPerformed

