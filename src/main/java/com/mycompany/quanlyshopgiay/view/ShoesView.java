    public Shoes getShoeFromInput() {
        try {
            String ma = txtMaGiay.getText().trim(); // Có thể rỗng, Controller sẽ set ID mới
            String ten = txtTenGiay.getText().trim();
            String hang = txtHang.getText().trim();
            String mauSac = txtMauSac.getText().trim();
            int size = (Integer) spinnerSize.getValue();
            int soLuong = (Integer) spinnerSoLuong.getValue();
            NumberFormat format = NumberFormat.getInstance(new Locale("vi", "VN"));
            double gia = format.parse(txtGia.getText().trim()).doubleValue();
            Date ngayNhap = dateChooserNgayNhap.getDate();
            String hinhAnh = txtHinhAnh.getText().trim();

            if (ten.isEmpty() || hang.isEmpty() || mauSac.isEmpty() || hinhAnh.isEmpty() || ngayNhap == null) {
                return null;
            }

            return new Shoes(ma, ten, hang, size, mauSac, gia, ngayNhap, soLuong, hinhAnh);
        } catch (Exception e) {
            return null;
        }
    }



